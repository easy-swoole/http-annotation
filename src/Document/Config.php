<?php

namespace EasySwoole\HttpAnnotation\Document;

class Config
{
    protected string $host = "";

    /**
     * @return string
     */
    public function getHost(): string
    {
        return $this->host;
    }

    /**
     * @param string $host
     */
    public function setHost(string $host): void
    {
        $this->host = $host;
    }

}